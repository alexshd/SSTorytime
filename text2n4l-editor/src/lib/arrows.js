export function isValidArrow(arrowText)
{
  const validArrows = getValidArrowsList();
  const cleanArrow = arrowText.replace(/^\(|\)$/g, '').trim().toLowerCase();

  return validArrows.some(valid =>
  {
    return cleanArrow === valid.toLowerCase();
  });
}
